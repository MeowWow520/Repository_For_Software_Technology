#include <iostream>
using namespace std;

/** #1. A + B Problem
 *  https://uoj.ac/problem/1
 * 
 *  ------问题描述------
 *  给你两个数α和b, 请输出他们的和

 *  ------输入格式------
 *  一行, 两个用空格隔开的整数 a 和 b

 *  ------输出格式------
 *  一个整数, 表示 a + b
 */

int a, b;
int main() {
	cin >> a >> b;
	cout << a + b;
	return 0;
}
